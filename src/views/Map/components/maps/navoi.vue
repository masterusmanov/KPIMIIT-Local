<script setup>
import { ref, watch, defineProps, defineEmits } from "vue";
import { useI18n } from "vue-i18n";

const { locale } = useI18n();

const props = defineProps({
  data: {
    type: Array,
    required: true,
  },
  active: {
    type: [String, Number],
    default: null,
  },
});
const emit = defineEmits(["changeDistrict"]);

const hoveredRegion = ref(null);

const getName = (id) => {
  // 1. Navoiy tumanlarining haqiqiy ID'lari bilan nomlari
  const districtNames = {
    1712211: { uz: "Konimex tumani", ru: "Канимехский район", en: "Konimex District" },
    1712238: { uz: "Nurota tumani", ru: "Нуратинский район", en: "Nurota District" },
    1712251: { uz: "Xatirchi tumani", ru: "Хатырчинский район", en: "Xatirchi District" },
    1712412: { uz: "Qozg'on tumani", ru: "Кызылкумский район", en: "Qozgon District" },
    1712234: { uz: "Karmana tumani", ru: "Карманинский район", en: "Karmana District" },
    1712216: { uz: "Qiziltepa tumani", ru: "Кызылтепинский район", en: "Qiziltepa District" },
    1712248: { uz: "Uchquduq tumani", ru: "Учкудукский район", en: "Uchquduq District" },
    1712408: { uz: "Zarafshon shahri", ru: "город Зарафшан", en: "Zarafshan City" },
    1712244: { uz: "Tomdi tumani", ru: "Тамдынский район", en: "Tomdi District" },
    1712230: { uz: "Navbahor tumani", ru: "Навбахорский район", en: "Navbahor District" }
  };

  // 2. API dan kelgan ma'lumotni tekshirish
  const district = props.data.find((district) => district.id == id);
  
  if (district) {
    // API dan nom kelgan bo'lsa
    if (district.district_name && district.district_name[locale.value]) {
      return district.district_name[locale.value];
    }
    // Yoki boshqa nom field'lari
    if (district.name && typeof district.name === 'object') {
      return district.name[locale.value];
    }
    if (district.name && typeof district.name === 'string') {
      return district.name;
    }
  }

  // 3. Qo'lda belgilangan nomlardan olish
  if (districtNames[id] && districtNames[id][locale.value]) {
    return districtNames[id][locale.value];
  }

  // 4. Fallback
  return `Tuman ${id}`;
};

const getPathStyle = (id) => {
  const district = props.data.find((district) => district.id == id);

  let className = "";

  if (district) {
    if (district.status == "good") {
      className += "text-ratingGood";
    } else if (district.status == "bad") {
      className += "text-ratingBad";
    } else {
      className += "text-ratingNormal";
    }
  }

  className += props.active == id ? " active" : "";

  if (hoveredRegion.value == id || props.active == id) {
    className += " active-path";
  } else if (hoveredRegion.value != null && props.active != id) {
    className += " inactive-path";
  }

  return className.trim();
};

watch(
  () => props.active,
  () => (hoveredRegion.value = null),
  { immediate: true }
);
</script>
