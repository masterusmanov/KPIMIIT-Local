import { useI18n } from "vue-i18n";

const { locale } = useI18n();

const props = defineProps({
  data: {
    type: Array,
    required: true,
  },
  active: {
    type: [String, Number],
    default: null,
  },
});
const emit = defineEmits(["changeDistrict"]);

const hoveredRegion = ref(null);

const getName = (id) => {
  // 1. Sirdaryo tumanlarining nomlari
  const districtNames = {
    1724226: { uz: "Sardoba tumani", ru: "Сардобинский район", en: "Sardoba District" },
    1724206: { uz: "Oqoltin tumani", ru: "Акалтынский район", en: "Oqoltin District" },
    1724212: { uz: "Boyovut tumani", ru: "Баяутский район", en: "Boyovut District" },
    1724228: { uz: "Mirzaobod tumani", ru: "Мирзаабадский район", en: "Mirzaabad District" },
    1724216: { uz: "Sayxunobod tumani", ru: "Сайхунабадский район", en: "Saykhunabad District" },
    1724231: { uz: "Sirdaryo tumani", ru: "Сырдарьинский район", en: "Syrdarya District" },
    1724220: { uz: "Guliston tumani", ru: "Гулистанский район", en: "Gulistan District" },
    1724401: { uz: "Guliston shahri", ru: "город Гулистан", en: "Gulistan City" },
