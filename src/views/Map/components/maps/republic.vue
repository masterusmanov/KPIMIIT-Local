<script setup>
import { ref, defineProps, defineEmits } from "vue";
import { useI18n } from "vue-i18n";

const props = defineProps({
  data: {
    type: Array,
    required: true,
  },
});
const emits = defineEmits(["changeRegion"]);

const { locale } = useI18n();

const hoveredRegion = ref(null);

const getPathColor = (region_id) => {
  const region = props.data.find((region) => region.region_id == region_id);

  if (region) {
    if (region.status == "good") {
      return "text-ratingGood";
    } else if (region.status == "normal") {
      return "text-ratingNormal";
    } else if (region.status == "bad") {
      return "text-ratingBad";
    }
  }
  return "text-ratingNormal";
};

const getName = (region_id) => {
  const region = props.data.find((region) => region.region_id == region_id);
  return region ? region.region_name[locale.value] : "Unknown Region";
};
